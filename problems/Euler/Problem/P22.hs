module Euler.Problem.P22 where
import           Data.Char                      ( ord )
import           Data.List                      ( sort )

ans :: IO Integer
ans = toInteger . sum . zipWith (flip scores) [1 ..] . parser <$> readFile
    "data/P22_names.txt"

parser :: String -> [String]
parser = sort . read . ("[" ++) . (++ "]")

scores :: String -> Int -> Int
scores = (*) . sum . map (\w' -> ord w' - ord 'A' + 1)
