-- A route has 40 steps, 20 down and 20 right, in 20x20 grid.  Thus, we have to
-- chose 20 steps of down and 20 steps of right.  Therefore, we have:
--
--  C(40 20) * C(20 20) = 40! / (20!(40-20)!) * 1
